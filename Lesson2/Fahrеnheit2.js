// Цельсий х 1,8 + 32 = Фаренгейт

const c = prompt('Введіть температуру в градусах Цельсія');

// Додано перевірку на порожнє значення або відміну

if (!c){
    alert("помилка! Не введено число");
} else {
    if (isFinite(c) || c == 0) {
        const f = c*1.8+32;
        alert(`Це те саме, що ${f} по Фаренгейту`);
    }
    else {
        alert("помилка! Введено не число");
    }
}
